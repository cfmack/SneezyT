<h1><i class="fa fa-cutlery"></i> Food</h1>
<p>
The Food section allows you to track things you eat or do not eat.  The Type section provides a list of previously entered data. If you do not see the data you are looking for, just type it out.  The system will add this data for your use next time.
</p>
<p>
The more detail you track, the better the system can correlate your reactions.   You can choose to just say "tortillas".  Instead, it is more powerful if you put "brown rice tortillas" and add "Food for Life" in the Notes.  If you track it too closely, it will be hard to make links to reactions.  If you track it too broadly, it will find too many links to reactions.
</p>
<p>
With Food Allergies or Eosinophilic Esophagitis (EoE), folks can use some estoteric foods.  Overtime, Sneezy T will have most of the food you use, customized for your usage.
</p>
