<h1><i class="fa fa-frown-o"></i> Reaction</h1>
<p>
    The Reaction section allows you to track things notice about you or the person you are tracking.  The Type section provides a list of previously entered data. If you do not see the data you are looking for, just type it out.  The system will add this data for your use next time.
</p>
<p>
    The more detail you track, the better the system can correlate your reactions to other data. You can track things like "rash", or "cough".  If you need to track something that can be measured like a "temperature", put "high temp" in the Type column and the degree (102) in the Amount.
</p>
<p>
    Sneezy T does not provide any medical advice.  It is just matching your inputs to other inputs.
</p>
<p>
    For more details on how to use this section, see these <a href="https://github.com/cfmack/SneezyT/wiki/Basic-Usage#wiki-inputting-data" target="_blank">instructions</a>.
</p>