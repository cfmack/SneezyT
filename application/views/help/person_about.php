<h1><i class="fa fa-user"></i> Person</h1>
<p>
Sneezy T allow you to track multiple users.  By default, when you sign up, you are given a single person to track.
This mirrors your log in information.   You can certainly change this with the Edit button in the list below.
</p>
<p>
    If you would like to change the user you are tracking in this session, use the Change Person option in the menu.
</p>
<p>
    To change the user you would like to load by default, you can accomplish this two ways.  Either add the new user with the default check box checked.
    Or add the Edit button to change the other Person to be the default.  You have to set the value to 1.  You will also need to set the other people to 0.
</p>