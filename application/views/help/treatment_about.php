<h1><i class="fa fa-medkit"></i> Treatment</h1>
<p>
    The Treatment section allows you to track measure you are taking to mitigate reactions.  The Type section provides a list of previously entered data. If you do not see the data you are looking for, just type it out.  The system will add this data for your use next time.
</p>
<p>
    This could be anything from "yoga" to "humidifier" to "doctor prescribed medicine". Sneezy T does not provide any medical advice.  Sneezy takes the same approach as <a href="http://www.patientslikeme.com">Patients like me</a>. It is just matching your inputs to other inputs.
</p>
<p>
    For more details on how to use this section, see these <a href="https://github.com/cfmack/SneezyT/wiki/Basic-Usage#wiki-inputting-data" target="_blank">instructions</a>.
</p>