<h1><i class="fa fa-cloud"></i> Environment</h1>
<p>
The Environment section allows you to track things in your environment.  The Type section provides a list of previously entered data. If you do not see the data you are looking for, just type it out.  The system will add this data for your use next time.
</p>
<p>
This could be something indoors or outdoors.   I have used <a href="http://pollen.com">Pollen.com</a> to track local allergens in the air.  It is not just pollen you can count.  You can add things like "cat dander" if you stayed in a house that had a cat.  Or even, "rain" to see if there is a correlation to mold.
</p>
<p>
For more details on how to use this section, see these <a href="https://github.com/cfmack/SneezyT/wiki/Basic-Usage#wiki-inputting-data" target="_blank">instructions</a>.
</p>
